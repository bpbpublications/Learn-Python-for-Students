# Simple Tkinter Application

# Import tkinter module 
import tkinter
# Create a new blank window using Tk() method of tkinter module  
window1 = tkinter.Tk()
# Write code below to add widgets.
# set window title
window1.wm_title("First Tkinter window")
# geometry() method defines the size of window
window1.geometry("300x200+10+20")
# show window
window1.mainloop()
