# Configure turtle pen and change its color

import turtle
turtle.showturtle()       # Displays turtle on screen
turtle.shape("square")     # changes turtle shape to square
turtle.pencolor("red")     # set pen color to red
turtle.penup()                 # pick up pen without drawing
turtle.fillcolor("blue")      # fills turtle shape to blue
turtle.pendown()            # pick down pen to draw
turtle.forward(100)        # move turtle forward by 100 units
turtle.home()         # moves turtle to screen center and point east
turtle.done()            # complete the execution
