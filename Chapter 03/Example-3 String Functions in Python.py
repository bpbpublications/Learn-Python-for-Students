# Various String functions in Python

# Demo of len(string)	
str2 ="Hello World"
print(len(str2))
# Output: 11

# Demo of max(string)	
str2 ="Hello World"
print(max(str2))
# Output: ‘r’

# Demo of min(string)	
str2 ="hello"
print(min(str2))
# Output: ’e’

# Demo of str(object)	
num = 101.24
s1 = str(num)
print(s1, type(s1))
# Output:101.24 <class 'str'>
