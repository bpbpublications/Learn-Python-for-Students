# Tuple Methods with examples

tuple1 = ('h', 'e', 'l', 'l', 'o')

# count(): This method counts and returns the number of times a specific value occurs in a tuple.
print(tuple1.count('l'))  
# Output:  2

# index(): Searches the tuple for a specified value and returns the position of the first occurrence of where the value was found.
print(tuple1.index('l'))  
# Output: 2

