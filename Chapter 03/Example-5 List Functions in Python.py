# Various List Functions with examples

numbers = [17, 11, 3, 7, 5, 2, 9]

# Demo of min(list_name): Returns the minimum value from a list in Python.
print(min(numbers))
# Output: 2

# Demo of max(list_name): Returns the largest value from a list in Python.
print(max(numbers))
# Output: 17

# Demo of len(list_name): Returns the number of elements in a list in Python.
numbers = [17, 11, 7, 5, 2, 9]
print(len(numbers))
# Output: 6

# Demo of list(sequence): Converts the sequence to a list in Python.
seq = "Hello ALl !!"
list1 = list(seq)
print(list1)
# Output: ['H', 'e', 'l', 'l', 'o', ' ', 'A', 'L', 'l', ' ', '!', '!']

