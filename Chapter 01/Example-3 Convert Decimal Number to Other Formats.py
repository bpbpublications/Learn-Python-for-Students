#  Program  to  convert  a  Decimal  number  into  other  number  formats
decimal_num  =  180
print("The  Decimal  Number  is    :  ",  decimal_num)
print("Converted  to  Binary  :  ",  bin(decimal_num))
print("Converted  to  Octal  :  ",  oct(decimal_num))
print("Converted  to  Hexadecimal  :  ",  hex(decimal_num))
