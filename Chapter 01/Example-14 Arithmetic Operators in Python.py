# Arithmetic Operators in Python
a = 10
b = 2

# Addition Operator
print ('Sum: ', a + b)
# Output Sum:  12

# Subtraction Operator
print ('Subtraction: ', a - b)
# Output Subtraction:  8   

# Multiplication Operator
print ('Multiplication: ', a * b)  
# Output Multiplication:  20

# Division Operator
print ('Division: ', a / b) 
# Output Division:  5.0

# Floor division Operator
print ('Floor Division: ', a // b)
# Output Floor Division:  5

# Modulo Operator
print ('Modulo: ', a % b)  
# Output Modulo:  0

# Power Operator
print ('Power: ', a ** b)
# Output Power:  100  