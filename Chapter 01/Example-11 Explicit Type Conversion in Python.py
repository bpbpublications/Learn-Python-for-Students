num_int = 101
num_float = 22.0
num_str = "123"
print("Orginal Datatype of num_int : ", type(num_int))
print("Orginal Datatype of num_float : ", type(num_float))
print("Orginal Datatype of num_str : ", type(num_str))
print(".....................Explicit Typecasting........................")
print("Datatype of num_int after Typecasting into String :",type(str(num_int)))
print("Datatype of num_float after Typecasting into String : ",type(str(num_float )))
print("Datatype of num_str after Typecasting into Integer : ", type(int(num_str)))
print("Datatype of num_str after Typecasting into Float : ", type(float(num_str)))
